void main(){
  int number=1;

  do{
    print('This will print even though number > 5');
    number++;
  }while(number<=3);

  int num=4;

  // A do-while loop will execute at least once,
  // the condition is true or false.

  do{
    print('This will print even though number > 10');
    number++;
  }while(num<=3);
}